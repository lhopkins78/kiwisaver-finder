// KiwiSaver Fund Finder — Frontend Logic

const fundData = []; // Populated from CSV/JSON

// Goal selections
function selectGoal(goal) {
    sessionStorage.setItem('ks-goal', goal);
    document.getElementById('step-type').classList.remove('hidden');
    window.scrollTo(0, document.getElementById('step-type').offsetTop - 20);
}

function selectType(type) {
    sessionStorage.setItem('ks-type', type);
    document.getElementById('step-age').classList.remove('hidden');
    window.scrollTo(0, document.getElementById('step-age').offsetTop - 20);
}

function showResults() {
    const age = document.getElementById('age').value;
    const goal = sessionStorage.getItem('ks-goal');
    const type = sessionStorage.getItem('ks-type');
    
    if (!age) {
        alert('Please enter your age');
        return;
    }
    
    sessionStorage.setItem('ks-age', age);
    document.getElementById('results').classList.remove('hidden');
    
    renderResults(age, goal, type);
    window.scrollTo(0, document.getElementById('results').offsetTop - 20);
}

// Load fund data from JSON (generated by analysis script)
async function loadFundData() {
    try {
        const response = await fetch('data/funds.json');
        if (response.ok) {
            const data = await response.json();
            fundData.splice(0, 0, ...data);
        }
    } catch (e) {
        console.log('Fund data not yet available — early development');
    }
}

function classifyAge(age) {
    // Infer fund type from age if user doesn't know
    age = parseInt(age);
    if (age < 35) return 'aggressive';
    if (age < 50) return 'growth';
    if (age < 60) return 'balanced';
    return 'moderate';
}

