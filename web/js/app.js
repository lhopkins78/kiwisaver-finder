function buildFundCard(fund) {
    const statusClass = fund.recommendation;
    const feeClass = parseFloat(fund.fee) > 0.7 ? 'high' : 'low';
    
    let reasonsHTML = '';
    if (fund.reasons) {
        reasonsHTML = '<div style="margin-top:8px;">';
        fund.reasons.forEach(r => {
            reasonsHTML += `<div style="font-size:0.82rem;color:#c0392b;">⚠️ ${r}</div>`;
        });
        reasonsHTML += '</div>';
    }
    
    return `
    <div class="fund-card ${statusClass}">
        <div class="fund-info">
            <h4>${fund.name}</h4>
            <div class="scheme">${fund.scheme}</div>
            <div class="fund-meta">
                <span>${fund.geo}</span>
                <span>${fund.currency}</span>
                <span>Passive</span>
            </div>
            ${reasonsHTML}
        </div>
        <div class="fund-stats">
            <div class="fee ${feeClass}">${fund.fee} fees</div>
            <div class="returns">5yr: ${fund.return5yr} (bench ${fund.benchmark})</div>
            ${fund.feeImpact ? `<div class="fee-impact">Costs ${fund.feeImpact} more vs passive over 30yr</div>` : ''}
        </div>
    </div>`;
}

